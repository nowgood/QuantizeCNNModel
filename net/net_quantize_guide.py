"""
guide 两个模型一起训练的两种思路,
    1. 将两个模型分别训练, 然后提取中间层的 feature map, 计算 distance
    2. 讲这两个模型写成一个网络, 一起训练, 加载参数时, 一起加载, 然后提取训练好的低精度模型的参数
"""

"""
1. 将卷积层, 除了第一层使用 QWConv2D(不量化输出, 不然性能下降10个百分点), 全部使用 QWACvon2D
2. 线性层全部使用 QWALinear, 线性层所占的参数比例在 resnet18中占据 4.4%, resnet50中占据 8%, 不量化的话会有大约 0.4个百分点的性能提升
3. 在全连接层送入 softmax 之前, 加一个标量层, 做 softmax 的软化??
"""

